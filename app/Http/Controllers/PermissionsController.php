    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        //
    }

    public function doValidation($request, $permission)
    {
        return $request->validate(
            [
                'name' =>  [($permission->id) ? "sometimes" : "required", Rule::unique(Permission::class)->ignore($permission->id)],
            ],
        );
    }
}
